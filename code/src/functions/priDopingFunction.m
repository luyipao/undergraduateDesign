function y = priDopingFunction(x)
cosFunc1 = @(x) -249 * cos(20*pi*(x-0.075)) / (20*pi);
cosFunc2 = @(x) -249 * cos(20*pi*(x-0.475)) / (20*pi);

y = @(x) (x>=0 & x<=0.1) .* (500*x) ...
    + (x>0.1 & x<0.15) .* (cosFunc1(x) + 251*x + 24.9) ...
    + (x>=0.15 & x<=0.45) .* (2*x +  62.25) ...
    + (x>0.45 & x<0.5) .* (cosFunc2(x) + 251*x - 49.8) ...
    + (x>=0.5 & x<=0.6) .* (500*x - 174.3);
% unit 10e12 cm^-3
y = y(x) * 10e2;

end
